const fs = require('fs');
const path = require('path');

const rootDir = path.resolve(__dirname, '..');
const candidatePaths = [
  path.join(rootDir, 'data', 'kntt-lop6-ontap-bank-v6.jsonl'),
  path.join(rootDir, 'data', 'kntt-lop6-ontap-bank-v5.jsonl'),
  path.join(rootDir, 'data', 'kntt-lop6-ontap-bank-v4.jsonl'),
  path.join(rootDir, 'data', 'kntt-lop6-ontap-bank-v3.jsonl'),
  path.join(rootDir, 'data', 'kntt-lop6-ontap-bank-v2.jsonl')
];
const sourcePath = candidatePaths.find((filePath) => fs.existsSync(filePath));
const outputDir = path.join(rootDir, 'src');
const outputPath = path.join(outputDir, 'question-bank.mjs');

function readJsonl(filePath) {
  const raw = fs.readFileSync(filePath, 'utf8').trim();
  if (!raw) return [];
  return raw.split(/\r?\n/).filter(Boolean).map((line) => JSON.parse(line));
}

function main() {
  if (!sourcePath) throw new Error('No question bank source found.');
  const questionBank = readJsonl(sourcePath);
  const moduleSource = [
    '// This file is generated by scripts/build-question-bank.js.',
    '// Do not edit it by hand.',
    '',
    `export default ${JSON.stringify(questionBank, null, 2)};`,
    ''
  ].join('\n');

  fs.mkdirSync(outputDir, { recursive: true });
  fs.writeFileSync(outputPath, moduleSource, 'utf8');
  console.log(`Generated ${questionBank.length} questions from ${path.basename(sourcePath)} -> ${outputPath}`);
}

main();
